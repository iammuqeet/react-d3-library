// Function to create links between adjacent companies

const companyLinks = []

function createLinks(companies) {
  for (let i = 0; i < companies.result.data.length - 1; i++) {
    const currentCompany = companies.result.data[i]
    const nextCompany = companies.result.data[i + 1]

    // Create a link object with source and destination IDs
    const link = {
      source: currentCompany._id,
      target: nextCompany._id,
    }

    // Add the link object to the current company's links array
    companyLinks.push(link)
  }
}

